"""Calculator Functions"""


def calculator_add(x, y):
    """ Adds x to y """
    return x + y


def calculator_subtract(x, y):
    """ Subtracts y from x """
    return x - y


def calculator_multiply(x, y):
    """ Multiplies x and y """
    return x * y


def calculator_divide(x, y):
    """ Divides x by y """
    return x/y


def calculator_squared(x):
    """ Squares x"""
    return x**2
