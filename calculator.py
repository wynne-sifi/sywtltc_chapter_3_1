


def calculator_add(x, y):
    """ Adds left_hand_number to right_hand_number """
    return x + y


def calculator_subtract(x, y):
    """ Subtracts y from x """
    return x - y


def calculator_multiply(x, y):
    """ Multiplies x and y """
    return x * y

def calculator_divide(x,y):
    """ Divides x by y """
    return x/y
